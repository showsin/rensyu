"""
编写一个程序来检查两个数字是否为二进制 异位词。
二进制异位词指的是两个数字的二进制表示中1的数量相同。

例如，考虑两个数字：5和6。 5的二进制表示为101，6的二进制表示为110。1的数量是相同的。因此，5和6是二进制异位词。

定义函数is_binary_anagram()，有两个参数num1和num2。
在函数内，将两个数字转换为其二进制表示，并检查它们是否为异位词。如果是，则返回True，否则返回False。
示例输入
12
6
示例输出
True
解释:
12的二进制表示为1100，6的二进制表示为0110。由于1的数量相同，因此12和6是二进制异位词。

使用bin()函数将数字转换为二进制表示。
使用count()函数计算字符串中某个字符的数量。
"""

def binary_anagram(num1, num2):
    # 此处编写代码
    return bin(num1).count('1') == bin(num2).count('1')
# 获取输入
num1 = int(input())
num2 = int(input())

# 调用函数
print(binary_anagram(num1, num2))